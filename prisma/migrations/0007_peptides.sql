-- =============================================================
-- peptide_reminders — dose / refill / lab / appointment reminders.
-- Marking a repeating reminder done advances due_at by
-- repeat_every_days; a one-off flips active to false.
-- =============================================================
create table if not exists public.peptide_reminders (
  id                uuid primary key default gen_random_uuid(),
  user_id           uuid not null references auth.users(id) on delete cascade,
  peptide_id        uuid references public.peptides(id) on delete set null,
  kind              text not null check (kind in ('DOSE','REFILL','LAB','APPOINTMENT')),
  title             text not null,
  due_at            timestamptz not null,
  repeat_every_days integer check (repeat_every_days > 0),
  active            boolean not null default true,
  notes             text,
  created_at        timestamptz not null default now(),
  updated_at        timestamptz not null default now()
);

create index if not exists peptide_reminders_user_active_due_idx
  on public.peptide_reminders (user_id, active, due_at);

alter table public.peptide_reminders enable row level security;

